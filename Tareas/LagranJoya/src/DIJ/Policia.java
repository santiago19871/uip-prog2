package DIJ;

public class Policia {
    private String nombre;
    private String rango;

    public void setNombre(String n) {
        this.nombre = n;
    }

    public String getNombre() {
        return this.nombre;
    }

    public void setRango(String rango) {
        this.rango = rango;
    }

    public String getRango() {
        return this.rango;

    }
}
